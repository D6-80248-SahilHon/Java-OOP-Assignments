
public class Pattern2 {

	public static void main(String[] args) {
		 int height=5;
	     
		 for(int i=1;i<=height;i++)
		 {
			 for(int j=1;j<=height-i;j++)
			 {
				 System.out.print(" ");
			 }
			 for(int k=1;k<=i;k++)
			 {
				 System.out.print("*");
			 }
			 for(int l=2;l<=i;l++)
			 {
				 System.out.print("*");
			 }
			 System.out.println();    
		}
		 
		 for(int i=1;i<height;i++)
		 {
			 for(int j=1;j<=i;j++)
			 {
				 System.out.print(" ");
			 }
			 for(int k=1;k<=height-i;k++)
			 {
				 System.out.print("*");
			 }
			 for(int l=i;l<=height-2;l++)
			 {
				 System.out.print("*");
			 }
			 System.out.println();    
		}
		  


	}

}
